#include<stdio.h>
#include<math.h>

int main(int argc, char const *argv[])
{
    printf("\nSQRT : %0.2f",sqrt(4));
    printf("\nPOW : %0.2f",pow(2,3));
    printf("\nAbsolute values : %d",abs(-25));
    printf("\nCeiling : %0.0f",ceil(2.1));
    printf("\nFloor : %0.0f",floor(2.7));
    printf("\nRound : %0.0f",round(2.4));
    return 0;
}
